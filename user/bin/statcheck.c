#include <rabbitbone_sys.h>

int main(int argc, char **argv) {
    (void)argc; (void)argv;
    au_stat_t st;
    au_memset(&st, 0, sizeof(st));
    if (au_stat("/etc/motd", &st) < 0) return 30;
    if (st.type != 1 || st.size == 0) return 31;
    au_i64 t0 = au_ticks();
    if (t0 < 0) return 32;
    const char *dir = "/tmp/user-statcheck-dir";
    au_unlink(dir);
    if (au_mkdir(dir) < 0) return 33;
    au_memset(&st, 0, sizeof(st));
    if (au_stat(dir, &st) < 0) return 34;
    if (st.type != 2) return 35;
    if (au_unlink(dir) < 0) return 36;
    au_statvfs_t sv;
    au_memset(&sv, 0, sizeof(sv));
    if (au_statvfs("/disk0", &sv) < 0) return 37;
    if (sv.block_size < 1024 || sv.total_blocks == 0 || sv.free_blocks > sv.total_blocks) return 38;
    au_i64 h = au_open("/disk0/hello.txt");
    if (h < 0) return 39;
    if (au_fsync(h) < 0) return 40;
    if (au_close(h) < 0) return 41;
    if (au_sync() < 0) return 42;
    const char *stage = "/tmp/user-install.stage";
    const char *final = "/tmp/user-install.bin";
    au_unlink(stage);
    au_unlink(final);
    if (au_create(final, "one", 3) < 0) return 43;
    if (au_create(stage, "two", 3) < 0) return 44;
    if (au_install_commit(stage, final) < 0) return 45;
    if (au_stat(stage, &st) >= 0) return 46;
    au_memset(&st, 0, sizeof(st));
    if (au_stat(final, &st) < 0 || st.size != 3) return 47;
    if (au_unlink(final) < 0) return 48;
    const char *pre = "/disk0/user-prealloc.bin";
    au_unlink(pre);
    if (au_create(pre, 0, 0) < 0) return 49;
    if (au_preallocate(pre, 8192) < 0) return 50;
    au_memset(&st, 0, sizeof(st));
    if (au_stat(pre, &st) < 0 || st.size != 8192) return 51;
    au_i64 preh = au_open(pre);
    if (preh < 0) return 52;
    char z[16];
    au_memset(z, 0x7f, sizeof(z));
    if (au_read(preh, z, sizeof(z)) != (au_i64)sizeof(z)) return 53;
    for (au_usize i = 0; i < sizeof(z); ++i) if (z[i] != 0) return 54;
    if (au_close(preh) < 0) return 55;
    if (au_unlink(pre) < 0) return 56;
    return 0;
}
